// Reponse numero 1
// --------- Itérer de 0 à 10 ---------// 
// --------- Boucle for ---------// 
for (let i = 0; i <= 10; i++) {
    console.log(i);
}


// --------- Boucle while ---------// 
let i = 0;
while (i <= 10) {
    console.log(i);
    i++;
}
// --------- Boucle do while ---------// 
let x = 0;
do {
    console.log(x);
    x++
} while (x <= 10)